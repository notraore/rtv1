#include "./includes/libft.h"

int		ft_atoi(const char *str)
{
	size_t	negative;
	size_t	i;
	int		res;

	negative = 0;
	i = 0;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative = 1;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10;
		res = res + str[i] - '0';
		i++;
	}
	if (negative == 1)
		return (-res);
	return (res);
}
